// WHILE AND FOR

let contador = 0;
while(contador <= 10){              // Se pone el limite, hasta donde se quiere llegar 
    console.log(contador);
    contador = contador + 1;           // Aumenta 1 cada bucle
}

console.log("Bucle For")

for(i = 0; i <= 10; i++){           //(valor inicial, limite, incrementador)
    console.log(i)
}